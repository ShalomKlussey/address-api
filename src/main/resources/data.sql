  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   country VARCHAR(255),
   region VARCHAR(255),
   city VARCHAR(255),
   code BIGINT,
   other VARCHAR(255),
   order_number VARCHAR(255),
   CONSTRAINT pk_address PRIMARY KEY (id)
);

